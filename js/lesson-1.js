// Запитуємо користувача ввести число від 1 до 4
let num = prompt('Введіть число від 1 до 4:');

let result; // Змінна для зберігання результату

// Використовуємо switch-case для визначення пори року
switch (num) {
  case '1':
    result = 'зима';
    break; // Зупиняємо виконання switch
  case '2':
    result = 'весна';
    break; // Зупиняємо виконання switch
  case '3':
    result = 'літо';
    break; // Зупиняємо виконання switch
  case '4':
    result = 'осінь';
    break; // Зупиняємо виконання switch
  default:
    // Цей блок виконається, якщо num не дорівнює '1', '2', '3' або '4'
    result = 'Вибачте, але ви маєте ввести значення від 1 до 4 включно';
  // break тут не обов'язковий, оскільки default - останній блок
}

// Виводимо результат в консоль
console.log(result);

